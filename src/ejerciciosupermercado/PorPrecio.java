    private void jtpreciomaximoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtpreciomaximoKeyReleased
        // TODO add your handling code here:
        
        //listar productos por rango de precio
        try{
            
            if (!jtpreciomaximo.getText().isEmpty() && !jtpreciominimo.getText().isEmpty()){
                
                Double pMenor=Double.parseDouble(jtpreciominimo.getText());
                Double pMayor=Double.parseDouble(jtpreciomaximo.getText());
                
                borrarFilas();
            
            for (Producto prod : Menu.listaProductos) {
               if(prod.getPrecio() > pMenor && prod.getPrecio() < pMayor ){
                    model.addRow(new Object[]{prod.getCodigo(), prod.getDescripcion(),prod.getPrecio(), prod.getStock()});
                }
                
            }
            }
            
           
            
            
           // para cargar a la tabla: model.addRow(new Object[]{pro.getCodigo(), pro.getDescripcion(),pro.getPrecio(), pro.getStock()}); 
        }catch(NumberFormatException e){
            
            JOptionPane.showMessageDialog(this, "Solo se admiten nÃ¹meros");
        }
    }//GEN-LAST:event_jtpreciomaximoKeyReleased


